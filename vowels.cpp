//Code for vowels and consonant using switch cases 

#include<bits/stdc++.h>
using namespace std;
int main(){
  char ch;
  cin>>ch;

  switch(ch){

    case 'a':
    cout<<"Vowels"<<endl;
    break;

    case 'e':
    cout<<"Vowels"<<endl;
    break;

    case 'i':
    cout<<"Vowels"<<endl;
    break;

    case 'o':
    cout<<"Vowels"<<endl;
    break;


    case 'u':
    cout<<"Vowels"<<endl;
    break;

    default:
    cout<<"Consonants"<<endl;
    break;

  }
  return 0;
  

}
